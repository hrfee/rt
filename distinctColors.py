# Colors brought in as hex values, and processed into individual elements with vim macros.
colors = [(0x8b, 0x45, 0x13), (0x19, 0x19, 0x70), (0x00, 0x80, 0x00), (0xbd, 0xb7, 0x6b), (0xb0, 0x30, 0x60), (0xff, 0x00, 0x00), (0xff, 0xa5, 0x00), (0xff, 0xff, 0x00), (0x7c, 0xfc, 0x00), (0x00, 0xfa, 0x9a), (0x00, 0xff, 0xff), (0x00, 0x00, 0xff), (0xff, 0x00, 0xff), (0x64, 0x95, 0xed), (0xee, 0x82, 0xee), (0xe6, 0xe6, 0xfa)] 

print(f"Vec3 distinctColors[{len(colors)}] = " + "{", end="")
for i, color in enumerate(colors):
    f = [float(int(v)) / 255.0 for v in color]
    print("{" + f"{f[0]:.3f}, {f[1]:.3f}, {f[2]:.3f}" + "}", end="")
    if i == len(colors)-1:
        print("};")
    else:
        print(", ", end="")
